CREATE TABLE Class
(
    id   SERIAL PRIMARY KEY,
    name VARCHAR
);

CREATE TABLE Student
(
    id          SERIAL PRIMARY KEY,
    first_name  VARCHAR,
    middle_name VARCHAR,
    last_name   VARCHAR,
    birthday    DATE,
    address     VARCHAR
);

CREATE TABLE Student_in_class
(
    id      SERIAL PRIMARY KEY,
    class   INT REFERENCES Class (id),
    student INT REFERENCES Student (id)
);

CREATE TABLE Teacher
(
    id          SERIAL PRIMARY KEY,
    first_name  VARCHAR,
    middle_name VARCHAR,
    last_name   VARCHAR
);

CREATE TABLE Subject
(
    id   SERIAL PRIMARY KEY,
    name VARCHAR
);

CREATE TABLE Timepair
(
    id         SERIAL PRIMARY KEY,
    start_pair TIME,
    end_pair   TIME
);

CREATE TABLE Schedule
(
    id          SERIAL PRIMARY KEY,
    date        DATE,
    class       INT REFERENCES Class (id),
    number_pair INT REFERENCES Timepair (id),
    teacher     INT REFERENCES Teacher (id),
    subject     INT REFERENCES Subject (id),
    classroom   INT
);

